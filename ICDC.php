<?php

    $x = 10;
    echo "++$x is =" . (++$x) . "\n"; // Outputs: 11
    echo "<br>Now x is =" . $x . "\n";   // Outputs: 11
    
    $x = 10;
    echo "$x++ is =" . ($x++) . "\n"; // Outputs: 10
    echo "<br>Now x is =" . $x . "\n";   // Outputs: 11
    
    $x = 10;
    echo "--$x is =" . (--$x) . "\n"; // Outputs: 9
    echo "<br>Now x is =" . $x . "\n";   // Outputs: 9
    
    $x = 10;
    echo "$x-- is =" . ($x--) . "\n";// Outputs: 10
    echo "<br>Now x is =" . $x;   // Outputs: 9
